    private void viewDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsButtonActionPerformed
        // View the details of the row selected
        int selectedRow = tableVitalSigns.getSelectedRow();
        if (selectedRow >= 0) {
            VitalSign vs = (VitalSign) tableVitalSigns.getValueAt(selectedRow, 0);
            tempTextField.setText(String.valueOf(vs.getTemperature()));
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            dateTextField.setText(vs.getDate());
            pulseTextField.setText(String.valueOf(vs.getPulse()));

        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_viewDetailsButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedRow = tableVitalSigns.getSelectedRow();
        if (selectedRow >= 0) {
            VitalSign vs = (VitalSign) tableVitalSigns.getValueAt(selectedRow, 0);
            vsh.deleteVitalSign(vs);
            JOptionPane.showConfirmDialog(null, "Vital Sign has been deleted");
            populateTable();
            bloodPressureTextField.setText("");
            dateTextField.setText("");
            pulseTextField.setText("");
            tempTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
        }

    }//GEN-LAST:event_deleteButtonActionPerformed


